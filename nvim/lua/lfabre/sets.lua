-- Sets
local set = vim.opt

set.number = true         -- Display line numbers
set.relativenumber= true 	-- Display realtine line numbers
set.tabstop = 2           -- Number of columns occupied by a tab
set.shiftwidth = 2        -- Autoindents with
set.autoindent = true     -- Indent new line with the same indentation
set.mouse = 'a'        	  -- Enable mouse click
set.hidden = true         -- Enables buffer for opened files
set.guicursor = ''		    -- Enables GUI Cursor (Block Cursor)
set.wrap = false		      -- Huge lines are not wrapped
set.swapfile = false		  -- Disable swap files
set.backup = false		    -- Disable backup files to be kept
set.incsearch = true		  -- Enables incremental search
set.scrolloff = 8		      -- Sets scroll offset
set.signcolumn = 'yes'		-- Adds a column behind line numbers (Used for line status)
set.colorcolumn = '100'		-- Warnning colomun (Big line alert)
